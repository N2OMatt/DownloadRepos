import os;


################################################################################
## Constants                                                                  ##
################################################################################
BASE_PATH = os.path.expanduser("~/Documents/Projects/");

ORGANIZATION_NAMES = [
    "N2OMatt",
    "N2OMattForks",
];


################################################################################
## Script                                                                     ##
################################################################################
def main():
    for org_name in ORGANIZATION_NAMES:
        cmd = "./download_repos.py {0} {1}".format(
            org_name, BASE_PATH
        );
        os.system(cmd);

if __name__ == '__main__':
    main()


